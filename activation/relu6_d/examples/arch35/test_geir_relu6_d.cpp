// CPU golden：y = min(max(x, 0), 6*scale)（独立于 kernel，double 直算）
static void ComputeGolden(const float* x, float* y, int64_t n, double scale)
{
    const double upper = 6.0 * scale;
    for (int64_t i = 0; i < n; ++i) {
        y[i] = static_cast<float>(std::min(std::max(static_cast<double>(x[i]), 0.0), upper));
    }
}

