// 计算多核切分 + UB 切分参数并填充 TilingData（dim0 > 0 场景）
static void ComputeTilingParams(Relu6DTilingData* tiling, int64_t dim0, ge::DataType dataType, uint64_t ubSize,
                                int64_t availableCoreNum)
{
    // 多核切分（每核 ≥ 4KB；blockFormer 512 元素对齐）
    int64_t elemBytes = static_cast<int64_t>(ge::GetSizeByDataType(dataType)); // fp16 → 2
    int64_t minDtypeBits = elemBytes * 8;                                      // fp16 → 16
    int64_t coreNum = (dim0 * minDtypeBits + MIN_TILING_BITS_SIZE_PER_CORE - 1) / MIN_TILING_BITS_SIZE_PER_CORE;
    coreNum = std::min(coreNum, availableCoreNum);
    int64_t blockFormer = CeilAlign(CeilDiv(dim0, coreNum), ELEM_ALIGN_FACTOR);
    int64_t blockNum = CeilDiv(dim0, blockFormer); // 工作核数（恒 ≤ coreNum）

    // UB 切分（byte 量纲；extraSize=ALIGN_256 预留；ubFormer 256B 对齐）
    int64_t bufferDivisor = BUFFER_NUM * elemBytes; // fp16 → 8
    int64_t maxElemNum = (static_cast<int64_t>(ubSize) - ALIGN_256) / bufferDivisor;
    int64_t alignFactor = ALIGN_256 * 8 / minDtypeBits; // = 256/elemBytes，fp16 → 128
    int64_t ubFormer = (maxElemNum / alignFactor) * alignFactor;

    // 首/尾 block 循环次数与尾部
    int64_t ubLoopOfFormerBlock = CeilDiv(blockFormer, ubFormer);
    int64_t ubTailOfFormerBlock = blockFormer - (ubLoopOfFormerBlock - 1) * ubFormer;
    int64_t blockTail = dim0 - (blockNum - 1) * blockFormer;
    int64_t ubLoopOfTailBlock = CeilDiv(blockTail, ubFormer);
    int64_t ubTailOfTailBlock = blockTail - (ubLoopOfTailBlock - 1) * ubFormer;
    tiling->coreNum = static_cast<int32_t>(coreNum);
    tiling->blockFormer = blockFormer;
    tiling->blockNum = blockNum;
    tiling->ubFormer = ubFormer;
    tiling->ubLoopOfFormerBlock = ubLoopOfFormerBlock;
    tiling->ubTailOfFormerBlock = ubTailOfFormerBlock;
    tiling->ubLoopOfTailBlock = ubLoopOfTailBlock;
    tiling->ubTailOfTailBlock = ubTailOfTailBlock;
}

