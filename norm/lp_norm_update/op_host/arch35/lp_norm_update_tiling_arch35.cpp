static float CalcEpsEff(ge::DataType dtype, float epsilon)
{
    if (dtype == ge::DT_FLOAT16) {
        if (epsilon <= 1e-7f) {
            // eps ≈ 0 → eps_eff = 0；否则 → eps_eff = 1e-7
            return (epsilon == 0.0f) ? 0.0f : 1e-7f;
        }
        return epsilon;
    }
    // FP32
    return epsilon;
}

// 设置 TilingKey（按 p 值分派计算路径）
static int32_t CalcTilingKey(float p, float* invP)
{
    if (IsPosInf(p) || IsNegInf(p) || p == P_ONE) {
        // p ∈ {1, ±inf} → 恒等路径
        *invP = P_ZERO;
        return TILING_KEY_IDENTITY;
    }
    if (p == P_TWO) {
        // p == 2 → Sqrt 快速路径
        *invP = INV_P_TWO;
        return TILING_KEY_SQRT;
    }
    if (p == P_ZERO) {
        // p == 0 → 校验阶段已拒绝，此处兜底防除零
        *invP = P_ZERO;
        return TILING_KEY_IDENTITY;
    }
    // 其他有限非零 p（含负值）→ Power 路径
    *invP = 1.0f / p;
    return TILING_KEY_POWER;
}

