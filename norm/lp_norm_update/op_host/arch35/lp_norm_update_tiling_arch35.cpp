struct TilingSplitResult {
    int64_t blockFactor;
    int64_t ubFactor;
    uint32_t powerTmpBufferSize;
};

static ge::graphStatus CalcTilingSplit(int64_t totalNum, int64_t coreNum, uint64_t ubSize, ge::DataType dataType,
                                       int32_t tilingKey, TilingSplitResult& result)
{
    // 多核切分
    int64_t elemBytes = (dataType == ge::DT_FLOAT16) ? FP16_TYPE_SIZE : FP32_TYPE_SIZE;
    int64_t minDtypeBits = elemBytes * 8;
    int64_t calcCoreNum = (totalNum * minDtypeBits + MIN_TILING_BITS - 1) / MIN_TILING_BITS;
    int64_t usedCoreNum = std::min(calcCoreNum, coreNum);
    result.blockFactor = CeilAlign(CeilDiv(totalNum, usedCoreNum), ELEM_ALIGN_FACTOR);
    usedCoreNum = CeilDiv(totalNum, result.blockFactor);

    // UB 切分
    int64_t bufferNum = 1;
    int64_t bufferDivisor;
    if (dataType == ge::DT_FLOAT16) {
        bufferDivisor = bufferNum * 2 * elemBytes + FP16_CAST_BUFFER_COUNT * FP32_TYPE_SIZE;
    } else {
        int64_t bufferCount = (tilingKey == TILING_KEY_IDENTITY) ? FP32_BASE_BUFFER_COUNT : FP32_EXTRA_BUFFER_COUNT;
        bufferDivisor = bufferNum * bufferCount * elemBytes;
    }

    // Power 路径 sharedTmpBuffer
    int64_t powerExtraSize = 0;
    int64_t powerPerElemDivisor = 0;
    uint32_t maxLiveNodeCount = 0;
    uint32_t extraBuffer = 0;
    if (tilingKey == TILING_KEY_POWER) {
        AscendC::GetPowerTmpBufferFactorSize(true, false, false, POWER_TYPE_SIZE, maxLiveNodeCount, extraBuffer);
        powerExtraSize = static_cast<int64_t>(extraBuffer);
        powerPerElemDivisor = static_cast<int64_t>(maxLiveNodeCount) * static_cast<int64_t>(POWER_TYPE_SIZE);
    }

    int64_t totalDivisor = bufferDivisor + powerPerElemDivisor;
    int64_t maxElemNum = (static_cast<int64_t>(ubSize) - powerExtraSize - UB_HEADROOM) / totalDivisor;
    int64_t alignFactor = ALIGN_256 / elemBytes;
    result.ubFactor = FloorAlign(maxElemNum, alignFactor);

    if (result.ubFactor <= 0) {
        return ge::GRAPH_FAILED;
    }

    if (tilingKey == TILING_KEY_POWER) {
        result.powerTmpBufferSize = maxLiveNodeCount * static_cast<uint32_t>(result.ubFactor) * POWER_TYPE_SIZE +
                                    extraBuffer;
    } else {
        result.powerTmpBufferSize = 0;
    }

    // 保存 usedCoreNum 到 blockFactor 对应的核数（通过外部 SetBlockDim）
    // 注意：usedCoreNum 通过返回值传递不够，这里用 blockFactor 的负数编码——不行
    // 改为：caller 从 blockFactor 反推 usedCoreNum = CeilDiv(totalNum, blockFactor)
    return ge::GRAPH_SUCCESS;
}

