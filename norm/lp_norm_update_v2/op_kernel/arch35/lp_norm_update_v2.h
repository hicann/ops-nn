    int32_t count = (rawCount + alignElems - 1) / alignElems * alignElems;
    if (count == 0) {
        count = alignElems;
    }

    // 编译时 P_BRANCH 选择分支（修复 B 类问题）
    // P_BRANCH 由 ASCENDC_TPL_SEL_PARAM 路由确定，每个实例只编译对应分支代码
    // 消除运行时 branchId_ 依赖，避免 TilingData 字段传递异常导致的分支选择错误
    //
    // x_pos 钳位值使用 FLT_MIN (1.1754943508e-38f, FP32 最小正规数)
    // 替代 1e-38f (非正规数)（修复 C 类问题）
