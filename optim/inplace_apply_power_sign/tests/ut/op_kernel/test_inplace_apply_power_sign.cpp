static void ComputeGolden(const float* var, const float* m, float lr, float logbase, float signDecay, float beta,
                          const float* grad, float* varOut, float* mOut, size_t n)
{
    for (size_t i = 0; i < n; i++) {
        float mNew = beta * m[i] + (1.0f - beta) * grad[i];
        float signM = (mNew > 0.f) ? 1.f : ((mNew < 0.f) ? -1.f : 0.f);
        float signG = (grad[i] > 0.f) ? 1.f : ((grad[i] < 0.f) ? -1.f : 0.f);
        float signGm = signM * signG;
        varOut[i] = var[i] - lr * std::exp(logbase * signDecay * signGm) * grad[i];
        mOut[i] = mNew;
    }
}

static void FillTilingData(InplaceApplyPowerSignTilingData* td, size_t numElements, int32_t ubFormer)
{
    memset(td, 0, sizeof(*td));
    td->dim0 = static_cast<int64_t>(numElements);
    td->coreNum = 1;
    td->ubFormer = ubFormer;
    td->blockFormer = static_cast<int64_t>(numElements);
    td->blockNum = 1;
    td->ubLoopOfFormerBlock = (static_cast<int64_t>(numElements) + ubFormer - 1) / ubFormer;
    td->ubTailOfFormerBlock = static_cast<int64_t>(numElements) - (td->ubLoopOfFormerBlock - 1) * ubFormer;
    td->ubLoopOfTailBlock = td->ubLoopOfFormerBlock;
    td->ubTailOfTailBlock = td->ubTailOfFormerBlock;
    td->elemNum = static_cast<int64_t>(numElements);
    td->scheMode = 0;
    td->dtypeSize = sizeof(float);
    td->sharedTmpSize = 2048;
}

