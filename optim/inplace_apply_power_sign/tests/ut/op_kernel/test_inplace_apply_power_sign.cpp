TEST_F(InplaceApplyPowerSignKernelTest, test_fp32_basic)
{
    constexpr size_t N = 256;
    size_t dataSize = N * sizeof(float);
    GmBufs buf;
    buf.Alloc(dataSize, sizeof(InplaceApplyPowerSignTilingData));

    float* varPtr = reinterpret_cast<float*>(buf.var);
    float* mPtr = reinterpret_cast<float*>(buf.m);
    float* gradPtr = reinterpret_cast<float*>(buf.grad);
    for (size_t i = 0; i < N; i++) {
        varPtr[i] = static_cast<float>(i % 7) * 0.1f - 0.3f;
        mPtr[i] = static_cast<float>(i % 5) * 0.05f - 0.1f;
        gradPtr[i] = static_cast<float>(i % 9) * 0.02f - 0.08f;
    }
    float lrVal = 0.01f, logbaseVal = 1.0f, signDecayVal = 0.5f, betaVal = 0.9f;
    buf.SetScalars(lrVal, logbaseVal, signDecayVal, betaVal);

    FillTilingData(reinterpret_cast<InplaceApplyPowerSignTilingData*>(buf.tiling), N, N);

    AscendC::SetKernelMode(KernelMode::AIV_MODE);
    ICPU_RUN_KF(inplace_apply_power_sign, 1, buf.var, buf.m, buf.lr, buf.logbase, buf.signDecay, buf.beta, buf.grad,
                buf.varOut, buf.mOut, buf.workspace, buf.tiling);

    float expectedVar[N], expectedM[N];
    ComputeGolden(varPtr, mPtr, lrVal, logbaseVal, signDecayVal, betaVal, gradPtr, expectedVar, expectedM, N);

    float* varOutPtr = reinterpret_cast<float*>(buf.varOut);
    float* mOutPtr = reinterpret_cast<float*>(buf.mOut);
    for (size_t i = 0; i < N; i++) {
        EXPECT_NEAR(varOutPtr[i], expectedVar[i], 1e-4f) << "var mismatch at " << i;
        EXPECT_NEAR(mOutPtr[i], expectedM[i], 1e-4f) << "m mismatch at " << i;
    }

    buf.Free();
}

