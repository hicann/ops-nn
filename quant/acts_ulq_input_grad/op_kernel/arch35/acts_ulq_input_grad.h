        AscendC::Reg::LoadAlign(yReg, yGradAddr + i * oneRepeatSize);
        AscendC::Reg::LoadAlign(minReg, minMaskAddr + i * oneRepeatSize);
        AscendC::Reg::LoadAlign(maxReg, maxMaskAddr + i * oneRepeatSize);
        AscendC::Reg::Mul(signalReg, minReg, maxReg, mask);
