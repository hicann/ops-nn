// UpdateMask<float>（较大 dtype 筛选，单一 mask 原则，穿刺 K3 实证）。
// v2.0 覆盖 K1(fp16+bool) / K3(fp32+bool) / K4(fp32+fp32)（K2 fp16+fp16 走 GateMulHalfVF）。
